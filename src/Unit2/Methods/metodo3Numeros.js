/**
 * Ejemplo de paso por valor en JavaScript
 * Demuestra cómo encontrar el número mayor entre tres valores
 */

// Función que encuentra el mayor de tres números usando Math.max
const encontrarMayor = (a, b, c) => Math.max(a, b, c);

// Ejecutar y mostrar el resultado
console.log("El número mayor es:", encontrarMayor(10, 20, 5));
